package org.spring_core.spring_core;

public class ZoombieGame implements Game {
    public void up(){
        System.out.println("Go Forward");
    }
    public void down(){
        System.out.println("Go Down");
    }
    public void left(){
        System.out.println("Go Left");
    }
    public void right(){
        System.out.println("Go Right");
    }

}
